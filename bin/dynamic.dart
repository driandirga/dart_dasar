/*
* Dynamic variable yang bisa menampung semua TypeData
==Best Practice==
* Format : TypeData namaVariable = isi value nya;
*/

void main() {
  //Value Int
  dynamic variable = 100;
  print(variable);

  //Value Bool
  variable = true;
  print(variable);

  //Value String
  variable = 'Drian';
  print(variable);
}
