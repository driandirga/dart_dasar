/*
* Di dart TypeData number ada dua yaitu int dan double 
* int untuk bilangan bulat (1)
* double untuk bilangan desimal (0.5)
==Best Practice==
* Penulisan : Menggunakan titik untuk double (0.5)
* Format : TypeData namaVariable = isi value nya;
*/

void main() {
  //Integer TypeData
  int number1 = 10;

  //Double TypeData
  double number2 = 10.5;

  //Num TypeData (Bisa digunakan dan dapat diubah value TypeData nya Int ke Double atau Dowble ke Int )
  num number3 = 10;
  number3 = 10.5;

  print(number1);
  print(number2);
  print(number3);
}
