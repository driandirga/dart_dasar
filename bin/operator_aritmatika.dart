/*
* Operator Aritmatika
* Tambah + , Kurang - , Kali * , Bagi / (Double), Bagi ~/ (Int), Modulo % . 
==Best Practice==
* Format : TypeData namaVariable = isi value nya;
*/

void main() {
  //Input
  var first = 10;
  var second = 3;
  //Tambah
  print(first + second);
  //Kurang
  print(first - second);

  //Kali
  print(first * second);

  //Bagi (Double)
  print(first / second);

  //Bagi (Int)
  print(first ~/ second);

  //Modulo
  print(first % second);
}
